#문자열로 출력하기
print('{}*{}={}'.format(2,1,2*1))
print('{}*{}={}'.format(2,2,2*2))
print('{}*{}={}'.format(2,3,2*3))

#변수를 만들어서 출력 #3단을 출력
num = 3
print('{}*{}={}'.format(num,1,num*1))
print('{}*{}={}'.format(num,2,num*2))
print('{}*{}={}'.format(num,3,num*3))

#입력을 받아서 출력
num = int(input("원하는 단을 입력하세요 >>"))  #num=int(num)
print('{}*{}={}'.format(num,1,num*1))
print('{}*{}={}'.format(num,2,num*2))
print('{}*{}={}'.format(num,3,num*3))

